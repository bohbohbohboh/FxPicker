function getUniqueRandoms(pool, count) {
  const clone = [...pool];
  const selected = [];
  for (let i = 0; i < count && clone.length > 0; i++) {
    const idx = Math.floor(Math.random() * clone.length);
    selected.push(clone.splice(idx, 1)[0]);
  }
  return selected;
}

